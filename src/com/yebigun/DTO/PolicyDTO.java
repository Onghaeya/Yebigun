package com.yebigun.DTO;

public class PolicyDTO {
private String seq;
private String rank;
private String year;
private String mobilization;
private String time;
private String subtime;
private String tseq;
public String getSeq() {
	return seq;
}
public void setSeq(String seq) {
	this.seq = seq;
}
public String getRank() {
	return rank;
}
public void setRank(String rank) {
	this.rank = rank;
}
public String getYear() {
	return year;
}
public void setYear(String year) {
	this.year = year;
}
public String getMobilization() {
	return mobilization;
}
public void setMobilization(String mobilization) {
	this.mobilization = mobilization;
}
public String getTime() {
	return time;
}
public void setTime(String time) {
	this.time = time;
}
public String getSubtime() {
	return subtime;
}
public void setSubtime(String subtime) {
	this.subtime = subtime;
}
public String getTseq() {
	return tseq;
}
public void setTseq(String tseq) {
	this.tseq = tseq;
}
}
