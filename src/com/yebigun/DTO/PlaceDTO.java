package com.yebigun.DTO;

public class PlaceDTO
{
      private String place;
      private String tel;
      private String toliet;
      private String water;
      private String aidkit;
      private String smokearea;
      private String tseq ;
      public String getPlace()
      {
            return place;
      }
      public void setPlace(String place)
      {
            this.place = place;
      }
      public String getTel()
      {
            return tel;
      }
      public void setTel(String tel)
      {
            this.tel = tel;
      }
      public String getToliet()
      {
            return toliet;
      }
      public void setToliet(String toliet)
      {
            this.toliet = toliet;
      }
      public String getWater()
      {
            return water;
      }
      public void setWater(String water)
      {
            this.water = water;
      }
      public String getAidkit()
      {
            return aidkit;
      }
      public void setAidkit(String aidkit)
      {
            this.aidkit = aidkit;
      }
      public String getSmokearea()
      {
            return smokearea;
      }
      public void setSmokearea(String smokearea)
      {
            this.smokearea = smokearea;
      }
      public String getTseq()
      {
            return tseq;
      }
      public void setTseq(String tseq)
      {
            this.tseq = tseq;
      }
}
