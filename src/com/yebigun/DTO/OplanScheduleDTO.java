package com.yebigun.DTO;

public class OplanScheduleDTO
{
      private String startdate;
      private String dayplan;
      private String detail;
      private String fseq;
      private String jseq;
      public String getStartdate()
      {
            return startdate;
      }
      public void setStartdate(String startdate)
      {
            this.startdate = startdate;
      }
      public String getDayplan()
      {
            return dayplan;
      }
      public void setDayplan(String dayplan)
      {
            this.dayplan = dayplan;
      }
      public String getDetail()
      {
            return detail;
      }
      public void setDetail(String detail)
      {
            this.detail = detail;
      }
      public String getFseq()
      {
            return fseq;
      }
      public void setFseq(String fseq)
      {
            this.fseq = fseq;
      }
      public String getJseq()
      {
            return jseq;
      }
      public void setJseq(String jseq)
      {
            this.jseq = jseq;
      }
}
